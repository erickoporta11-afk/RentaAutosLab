    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (modo == 2) {
            int resp = javax.swing.JOptionPane.showConfirmDialog(this,
                    "¿Desea borrar el cliente seleccionado?");
            if (resp == 0) {
                listaCli.remove(indexEditar);
                javax.swing.JOptionPane.showMessageDialog(this, "Cliente eliminado");
                dispose();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void cargarDatos(logica.Cliente cli) {
        txtCedula.setText(String.valueOf(cli.getCedula()));
        txtCedula.setEditable(false); // no se debe poder cambiar la cédula al editar
        txtNombre.setText(cli.getNombre());
        txtFechaNac.setText(cli.getFechaNac().toString());
        cmbGenero.setSelectedItem(cli.getGenero());
        txtTelefono.setText(String.valueOf(cli.getTelefono()));
        txtDireccion.setText(cli.getDireccion());
    }

