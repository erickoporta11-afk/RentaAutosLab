    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            int cedula = Integer.parseInt(txtCedula.getText().trim());
            String nombre = txtNombre.getText().trim();
            java.time.LocalDate fecha = java.time.LocalDate.parse(txtFechaNac.getText().trim());
            String genero = cmbGenero.getSelectedItem().toString();
            int telefono = Integer.parseInt(txtTelefono.getText().trim());
            String direccion = txtDireccion.getText().trim();

            if (nombre.isEmpty() || direccion.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Debe completar todos los campos");
                return;
            }

            logica.Cliente cli = new logica.Cliente();
            cli.setCedula(cedula);
            cli.setNombre(nombre);
            cli.setFechaNac(fecha);
            cli.setGenero(genero);
            cli.setTelefono(telefono);
            cli.setDireccion(direccion);

            if (modo == 1) {
                listaCli.add(cli);
                javax.swing.JOptionPane.showMessageDialog(this, "Cliente agregado");
            } else if (modo == 2) {
                listaCli.set(indexEditar, cli);
                javax.swing.JOptionPane.showMessageDialog(this, "Cliente actualizado");
            }
            dispose();

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Cédula y Teléfono deben ser numéricos");
        } catch (java.time.format.DateTimeParseException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Formato de fecha inválido (use yyyy-MM-dd)");
        }
    }

